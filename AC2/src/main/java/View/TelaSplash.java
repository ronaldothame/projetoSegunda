    public TelaSplash() {
        initComponents();
        TelaAutenticacao login = new TelaAutenticacao();
        new Thread() {
            public void run() {
                try {
                    for (int i = 0; i <= 100; i++) {
                        Thread.sleep(100);
                        jpb_carregando.setValue(i);
                        if (jpb_carregando.getValue() <= 25) {
                            lblStatus.setText("Carregando sistema...");
                        } else if (jpb_carregando.getValue() <= 50) {
                            lblStatus.setText("Carregando banco de dados...");
                        } else if (jpb_carregando.getValue() <= 75) {
                            lblStatus.setText("Carregando tabelas...");
                        } else if (jpb_carregando.getValue() <= 99) {
                            lblStatus.setText("O sistema estÃ¡ sendo iniciado...");
                        } else {
                            Menu menu = new Menu();
                            menu.setVisible(true);
                            dispose();
                            
                        }
                    }
                } catch (InterruptedException ex) {
                }
            }
        }.start();
    }

